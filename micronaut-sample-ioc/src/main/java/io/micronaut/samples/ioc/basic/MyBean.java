package io.micronaut.samples.ioc.basic;

import javax.inject.Singleton;

@Singleton
public class MyBean {
    private Integer age;
    private String name;

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
